// ---------------------------------------------------------------------------

AppleIIVideo::AppleIIVideo() {
    fb_.fill(0);
}

void AppleIIVideo::reset() {
    textMode_   = true;
    page2_      = false;
    hiRes_      = false;
    mixed_      = false;
    clockCnt_   = 0;
    frameCount_ = 0;
    flashState_ = false;
    dirty_      = false;
    fb_.fill(0);
}

// ============================================================================
// clock — one cycle per call
// ============================================================================

void AppleIIVideo::clock() {
    if (++clockCnt_ >= kCyclesPerFrame) {
        clockCnt_ = 0;
        ++frameCount_;
        if ((frameCount_ & 0x1F) == 0) flashState_ = !flashState_;
        renderFrame();
        dirty_ = true;
    }
}

// ============================================================================
// fillRect
// ============================================================================

void AppleIIVideo::fillRect(int x, int y, int w, int h, uint8_t r, uint8_t g, uint8_t b) {
    for (int py = y; py < y + h && py < HEIGHT; ++py) {
        for (int px = x; px < x + w && px < WIDTH; ++px) {
            uint8_t* p = &fb_[(py * WIDTH + px) * 4];
            p[0] = r; p[1] = g; p[2] = b; p[3] = 0xFF;
        }
    }
}

// ============================================================================
// putChar — draw character ch at text column col, row in the framebuffer.
// Screen byte interpretation:
//   $00-$3F: inverse video (bg/fg swapped)
//   $40-$7F: flash (treated as normal on IIe)
//   $80-$FF: normal video
// Character index = screen_byte & 0x7F (maps directly to ASCII for $80-$FF).
// ---------------------------------------------------------------------------
void AppleIIVideo::putChar(int col, int row, uint8_t ch) {
    const bool inverse = (ch < 0x40);
    const bool flash   = (ch >= 0x40 && ch < 0x80);
    const uint8_t charIdx = ch & 0x7F;

    const uint8_t fgR = (inverse || (flash && flashState_)) ? kBgR : kFgR;
    const uint8_t fgG = (inverse || (flash && flashState_)) ? kBgG : kFgG;
    const uint8_t fgB = (inverse || (flash && flashState_)) ? kBgB : kFgB;
    const uint8_t bgR = (inverse || (flash && flashState_)) ? kFgR : kBgR;
    const uint8_t bgG = (inverse || (flash && flashState_)) ? kFgG : kBgG;
    const uint8_t bgB = (inverse || (flash && flashState_)) ? kFgB : kBgB;

    const int px0 = col * 7;
    const int py0 = row * 8;

    for (int scanRow = 0; scanRow < 8; ++scanRow) {
        const uint8_t bits = kFont[charIdx & 0x7F][scanRow];
        for (int bit = 0; bit < 7; ++bit) {
            const bool lit = (bits >> (6 - bit)) & 1;
            uint8_t* p = &fb_[((py0 + scanRow) * WIDTH + px0 + bit) * 4];
            p[0] = lit ? fgR : bgR;
            p[1] = lit ? fgG : bgG;
            p[2] = lit ? fgB : bgB;
            p[3] = 0xFF;
        }
    }
}

// ============================================================================
// renderText40 — 40-column text mode for rows [startRow, endRow)
// Apple II text screen addressing:
//   addr = page + (row >> 3) * 40 + (row & 7) * 128 + col
// ============================================================================

void AppleIIVideo::renderText40(uint16_t page, int startRow, int endRow) {
    if (!bus_) return;
    for (int row = startRow; row < endRow; ++row) {
        for (int col = 0; col < 40; ++col) {
            const uint16_t addr = page
                + static_cast<uint16_t>((row >> 3) * 40 + (row & 7) * 128 + col);
            putChar(col, row, bus_->read(addr));
        }
    }
}

// ============================================================================
// renderHiRes — monochrome hi-res graphics for rows [startRow, endRow)
// Each row: 40 bytes × 7 bits = 280 pixels.
// Addressing: addr = page + (row >> 6)*40 + ((row>>3)&7)*128 + (row&7)*1024 + byte
// ============================================================================

void AppleIIVideo::renderHiRes(uint16_t page, int startRow, int endRow) {
    if (!bus_) return;
    for (int row = startRow; row < endRow; ++row) {
        const uint16_t rowBase = page
            + static_cast<uint16_t>((row >> 6) * 40 + ((row >> 3) & 7) * 128 + (row & 7) * 1024);
        int px = 0;
        for (int byteIdx = 0; byteIdx < 40; ++byteIdx) {
            const uint8_t b = bus_->read(rowBase + static_cast<uint16_t>(byteIdx));
            for (int bit = 0; bit < 7; ++bit, ++px) {
                const bool lit = (b >> bit) & 1;
                uint8_t* p = &fb_[(row * WIDTH + px) * 4];
                p[0] = lit ? kFgR : kBgR;
                p[1] = lit ? kFgG : kBgG;
                p[2] = lit ? kFgB : kBgB;
                p[3] = 0xFF;
            }
        }
    }
}

// ============================================================================
// renderFrame
// ============================================================================

void AppleIIVideo::renderFrame() {
    const uint16_t textPage  = page2_ ? 0x0800 : 0x0400;
    const uint16_t hiResPage = page2_ ? 0x4000 : 0x2000;

    if (textMode_) {
        renderText40(textPage, 0, 24);
    } else if (hiRes_) {
        if (mixed_) {
            renderHiRes (hiResPage, 0,  20);
            renderText40(textPage,  20, 24);
        } else {
            renderHiRes(hiResPage, 0, 24);
        }
    } else {
        // Lo-res: same page as text — render as text for now
        if (mixed_) {
            renderText40(textPage, 0,  20);
            renderText40(textPage, 20, 24);
        } else {
            renderText40(textPage, 0, 24);
        }
    }
}

// ============================================================================
// Panel
// ============================================================================

void AppleIIVideo::drawPanel(const char* title, bool* open) {
    ImGui::SetNextWindowSize({ 280.0f, 160.0f }, ImGuiCond_FirstUseEver);
    if (!ImGui::Begin(title, open)) { ImGui::End(); return; }

    if (ImGui::CollapsingHeader("Video Mode", ImGuiTreeNodeFlags_DefaultOpen)) {
        ImGui::Text("Mode:   %s", textMode_ ? "Text" : (hiRes_ ? "Hi-Res" : "Lo-Res"));
        ImGui::Text("Page:   %d", page2_ ? 2 : 1);
        ImGui::Text("Mixed:  %s", mixed_ ? "Yes" : "No");
        ImGui::Text("Frame:  %d", frameCount_);
    }

    ImGui::End();
}

std::string AppleIIVideo::statusLine() const {
    std::ostringstream ss;
    ss << (textMode_ ? "TEXT" : (hiRes_ ? "HIRES" : "LORES"))
       << "  PAGE" << (page2_ ? 2 : 1)
       << (mixed_ ? "  MIXED" : "");
    return ss.str();
}
